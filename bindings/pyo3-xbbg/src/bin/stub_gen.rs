const CORE_STUB_SDK_VERSION: &str =
    "def sdk_version() -> tuple[builtins.int, builtins.int, builtins.int, builtins.int]: ...\n";
const CORE_STUB_ENABLE_SDK_LOGGING: &str =
    "def enable_sdk_logging(level: builtins.str) -> None:\n    r\"\"\"Enable Bloomberg SDK logging using the given level string.\"\"\"\n";
const CORE_STUB_EXPORT_PATCHES: [(&str, &str, &str); 2] = [
    (
        "\"enable_sdk_logging\",",
        "\"get_log_level\",",
        "\"enable_sdk_logging\",\n    \"get_log_level\",",
    ),
    (
        "\"sdk_version\",",
        "\"set_log_level\",",
        "\"sdk_version\",\n    \"set_log_level\",",
    ),
];
const CORE_STUB_DECL_PATCHES: [(&str, &str); 2] = [
    ("def enable_sdk_logging(", CORE_STUB_ENABLE_SDK_LOGGING),
    ("def sdk_version(", CORE_STUB_SDK_VERSION),
];

fn package_dir() -> PathBuf {
    let manifest_dir =
        env::var("CARGO_MANIFEST_DIR").unwrap_or_else(|_| env!("CARGO_MANIFEST_DIR").to_string());
    PathBuf::from(manifest_dir)
        .parent()
        .and_then(|path| path.parent())
        .expect("bindings/pyo3-xbbg should have a repo root parent")
        .join("py-xbbg/src/xbbg")
}

fn ensure_replacement(content: &mut String, needle: &str, anchor: &str, replacement: &str) {
    if !content.contains(needle) {
        *content = content.replace(anchor, replacement);
    }
}

fn ensure_trailing_declaration(content: &mut String, signature: &str, declaration: &str) {
    if !content.contains(signature) {
        content.push('\n');
        content.push_str(declaration);
    }
}

fn normalize_core_stub(mut core_stub: String) -> String {
    for (needle, anchor, replacement) in CORE_STUB_EXPORT_PATCHES {
        ensure_replacement(&mut core_stub, needle, anchor, replacement);
    }
    for (signature, declaration) in CORE_STUB_DECL_PATCHES {
        ensure_trailing_declaration(&mut core_stub, signature, declaration);
    }
    core_stub
}

fn normalize_generated_stubs() -> Result<()> {
    let package_dir = package_dir();
    fs::write(package_dir.join("__init__.pyi"), PACKAGE_INIT_STUB)?;

    let core_stub_path = package_dir.join("_core/__init__.pyi");
    let core_stub = normalize_core_stub(fs::read_to_string(&core_stub_path)?);

    fs::write(core_stub_path, core_stub)?;
    Ok(())
}

fn main() -> Result<()> {
    let stub = _core::stub_info()?;
    stub.generate()?;
    normalize_generated_stubs()?;
    Ok(())
}

#[cfg(test)]
mod tests {
    use super::{
        normalize_core_stub, CORE_STUB_ENABLE_SDK_LOGGING, CORE_STUB_EXPORT_PATCHES,
        CORE_STUB_SDK_VERSION,
    };

    #[test]
    fn normalize_core_stub_adds_missing_exports_and_declarations() {
        let input = r#"__all__ = [
    "get_log_level",
    "set_log_level",
]

def get_log_level() -> builtins.str:
    ...

def set_log_level(level: builtins.str) -> None:
    ...
"#;

        let output = normalize_core_stub(input.to_string());

        assert!(output.contains("\"enable_sdk_logging\","));
        assert!(output.contains("\"sdk_version\","));
        assert!(output.contains(CORE_STUB_ENABLE_SDK_LOGGING.trim_end()));
        assert!(output.contains(CORE_STUB_SDK_VERSION.trim_end()));
    }

    #[test]
    fn normalize_core_stub_is_idempotent() {
        let mut input = String::new();
        for (needle, _anchor, _replacement) in CORE_STUB_EXPORT_PATCHES {
            input.push_str(needle);
            input.push('\n');
        }
        input.push_str(CORE_STUB_ENABLE_SDK_LOGGING);
        input.push_str(CORE_STUB_SDK_VERSION);

        let output = normalize_core_stub(input.clone());

        assert_eq!(output.matches("\"enable_sdk_logging\",").count(), 1);
        assert_eq!(output.matches("\"sdk_version\",").count(), 1);
        assert_eq!(output.matches("def enable_sdk_logging(").count(), 1);
        assert_eq!(output.matches("def sdk_version(").count(), 1);
    }
}
